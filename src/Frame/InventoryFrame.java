    private void FrameInventory() {

        try {

            String sqlQuery = "select * from Inventory";
            pst = conn.prepareStatement(sqlQuery);
            rs = pst.executeQuery();
            InventoryRecievedTable.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }

    private void BoxCategory() {
        try {
            String sqlQuery = "SELECT Category from Inventory group by category";
            pst = conn.prepareStatement(sqlQuery);
            rs = pst.executeQuery();
            while (rs.next()) {
                String cat = rs.getString("Category");
                if (cat != null) {
                    InventoryCategory.addItem(cat);
                    EditCategory.addItem(cat);
                    EditCategory1.addItem(cat);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

    public void toExcel(JTable table, File file) {
        try {/*
             if (file.getParentFile().mkdir()) {
             file.createNewFile();
             }*/


            TableModel model = InventoryRecievedTable.getModel();
            //TableModel model2 = table2.getModel();
            try (FileWriter excel = new FileWriter(file)) {
                for (int i = 0; i < model.getColumnCount(); i++) {
                    excel.write(model.getColumnName(i) + ",");
                }

                excel.write("\n");

                for (int i = 0; i < model.getRowCount(); i++) {
                    for (int j = 0; j < model.getColumnCount(); j++) {
                        //System.out.println(model.getValueAt(i, j).toString());
                        excel.write(model.getValueAt(i, j).toString() + ",");
                    }
                    excel.write("\n");
                }
            }

        } catch (IOException e) {
        }

    }

